#Faça um programa que peça um valor e mostre se é positivo ou negativo

print('--- Programa que verifica se um número é positivo ou negativo ---')

data1=float(input('--- Por favor digite um número --- \n'))

if data1 > 0:
    print('Seu número', data1 ,'É positivo')
else:
    print('Seu número', data1 , 'É negativo')
